package com.itis.merch.core.enums;

/**
 * Represents the possible status values for a cart order, including CART, PENDING, PAYED, and CLOSED.
 * This enum provides a set of constant values that can be used to represent the status of a cart order.
 */
public enum CartOrderStatus {
	CART,
	PENDING,
	PAYED,
	CLOSED
}
